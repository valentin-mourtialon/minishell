char	*ft_strchr(char *s, int c)
{
	int		i;
	char	*pt;

	i = 0;
	if (!s)
		return (0);
	if (c > 256)
		c %= 256;
	while (s[i])
	{
		if (s[i] == c)
		{
			pt = &s[i];
			return (pt);
		}
		i++;
	}
	if (s[i] == c)
	{
		pt = &s[i];
		return (pt);
	}
	return (0);
}
